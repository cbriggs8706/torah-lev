export function normalizeSidebarLocale(value?: string | null): SidebarLocale {
	if (value === 'es' || value === 'he' || value === 'el') {
		return value
	}

	return 'en'
}

export function getSidebarLabel(
	locale: SidebarLocale,
	key: SidebarTranslationKey,
): string {
	return translations[locale][key]
}
